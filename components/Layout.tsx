import { PropsWithChildren, useEffect } from 'react'
import Head from 'next/head'
import clsx from 'clsx'
import { Inter } from '@next/font/google'
import { useAtom } from 'jotai'
import { atomWithStorage } from 'jotai/utils'

const darkModeAtom = atomWithStorage('darkMode', false)

const inter = Inter({ subsets: ['latin'] })

const Layout = ({ children }: PropsWithChildren) => {
  const [darkMode, setDarkMode] = useAtom(darkModeAtom)

  useEffect(() => {
    darkMode && document.body.classList.add('dark')
    !darkMode && document.body.classList.remove('dark')
  }, [darkMode])

  return (
    <div
      className={clsx(
        'flex min-h-screen flex-col transition-all duration-200 ease-linear dark:bg-primary-black',
        inter.className
      )}
    >
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className="container py-10">
        <button
          className="button my-4 w-max"
          onClick={() => setDarkMode(!darkMode)}
        >
          toggle theme
        </button>
        {children}
      </main>
      <footer className="mt-auto flex justify-center gap-3">
        <p>hello</p>
        <p>hello</p>
        <p>hello</p>
        <p>hello</p>
      </footer>
    </div>
  )
}

export default Layout
